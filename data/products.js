// Helper functions for filtering and searching
export const getProductsByCategory = (category) => {
  return products.filter(product => product.category === category);
};

export const getProductsBySubcategory = (subcategory) => {
  return products.filter(product => product.subcategory === subcategory);
};

export const searchProducts = (searchTerm) => {
  const term = searchTerm.toLowerCase();
  return products.filter(product => 
    product.name.toLowerCase().includes(term) ||
    product.description.toLowerCase().includes(term) ||
    product.tags.some(tag => tag.includes(term)) ||
    product.category.toLowerCase().includes(term) ||
    product.subcategory.toLowerCase().includes(term)
  );
};

export const getPopularProducts = () => {
  return products.filter(product => product.isPopular || product.isBestSeller);
};

export const getPremiumProducts = () => {
  return products.filter(product => product.isPremium);
};

export const getProductById = (id) => {
  return products.find(product => product.id === id);
};

